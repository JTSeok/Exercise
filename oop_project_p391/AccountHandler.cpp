void AccountHandler::makeAccount(void)
{
    int sel;
    cout << "-- 계좌 종류 선택 --" << endl;
    cout << "1. 보통예금계좌" << endl;
    cout << "2. 신용신뢰계좌" << endl;
    cout << "선택 : ";
    cin >> sel;

    if (sel == NORMAL)
        makeNormalAccount();
    else
        makeSpecialAccount();
}

void AccountHandler::makeNormalAccount(void)
{
    int id;
    char name[NAME_LEN];
    int balance;
    int interRate;

    cout << "-- 보통예금계좌 개설 --" << endl;
    cout << "계좌ID : "; cin >> id;
    cout << "이름 : "; cin >> name;
    cout << "입금액 : "; cin >> balance;
    cout << "이자율 : "; cin >> interRate;
    cout << endl;

    accArr[accNum++] = new NormalAccount(id, balance, name, interRate);
}

void AccountHandler::makeSpecialAccount(void)
{
    int id;
    char name[NAME_LEN];
    int balance;
    int interRate;
    int creditLevel;

    cout << "-- 신용신뢰계좌 개설 --" << endl;
    cout << "계좌ID : "; cin >> id;
    cout << "이름 : "; cin >> name;
    cout << "입금액 : "; cin >> balance;
    cout << "이자율 : "; cin >> interRate;
    cout << "신용등급(1toA, 2toB, 3toC) : "; cin >> creditLevel;
    cout << endl;

    switch (creditLevel)
    {
    case 1:
        accArr[accNum++] = new HighCreditAccount(id, balance, name, interRate, LEVEL_A); break;
    case 2:
        accArr[accNum++] = new HighCreditAccount(id, balance, name, interRate, LEVEL_B); break;
    case 3:
        accArr[accNum++] = new HighCreditAccount(id, balance, name, interRate, LEVEL_C); break;
    }
}

void AccountHandler::depositMoney(void)
{
    int money;
    int id;

    cout << "-- 입금 --" << endl;
    cout << "계좌ID: "; cin >> id;
    cout << "입금액: "; cin >> money;

    for (int i = 0; i < accNum; i++) {
        if (accArr[i]->getAccID() == id) {
            accArr[i]->deposit(money);
            cout << "입금완료" << endl;
            return;
        }
    }
    cout << "유효하지 않은 ID입니다." << endl << endl;
}

void AccountHandler::withrawMoney(void)
{
    int money;
    int id;

    cout << "-- 출금 --" << endl;
    cout << "계좌ID: "; cin >> id;
    cout << "출금액: "; cin >> money;

    for (int i = 0; i < accNum; i++) {
        if (accArr[i]->getAccID() == id) {
            if (accArr[i]->withdraw(money) == 0) {
                cout << "잔액부족" << endl << endl;
                return;
            }                
            cout << "출금완료" << endl;
            return;
        }
    }
    cout << "유효하지 않은 ID입니다." << endl << endl;
}

void AccountHandler::showAllAccInfo(void) const
{
    for (int i = 0; i < accNum; i++) {
        accArr[i]->showAccInfo();
        cout << endl;
    }
}

AccountHandler::~AccountHandler()
{
    for (int i = 0; i < accNum; i++)
        delete accArr[i];
}